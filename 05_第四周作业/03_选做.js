// 数组中有没有“两个数字之间的数字和，等于它俩本身的和”，请寻找这样的数组。比如数组[5,5,8,1,9,4,6,1]中，5+9等于14，5+8+1也等于14；所以[5,5,8,1,9]是符合要求的；另外，[8,1,9,4,6]也是符合要求的，因为8+6是14,1+9+4也是14。


var arr = [5, 5, 8, 1, 9, 4, 6, 1];
for (var i = 0; i < arr.length - 2; i++) {
    var sum = 0;
    for (var j = i + 2; j < arr.length; j++) {
        sum += arr[j - 1]
        if (arr[i] + arr[j] == sum) {
            console.log(arr.slice(i, j + 1));
        }
    }

}
